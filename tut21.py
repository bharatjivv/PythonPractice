# Arithmetic Operators
print("5 + 6 is", 5+6)  # Addition
print("5 - 6 is", 5-6)  # Subtraction
print("5 * 6 is", 5*6)  # Multiplication
print("5 ** 3 is", 5**3)  # 5 Raised to the power 3
print("5 % 3 is", 5%3)  # Modulus Operator
print("5 / 6 is", 5/6)  # Division
print("5 // 6 is", 5//6)    # Gives only the integer value


# Assignment Operators
print("Assignment operators")
x = 5
print(x)
x+=7
print(x)

# Comparison Operators
print("comparison operators")
i = 5
print(i == 5)
print(i >= 5)
print(i <= 5)

# Logical Operators
a = True
b = False
print("Logical operators")
print(a and b)
print(a or b)
print(a is b)
print(a is not b)
print(5 is not 7)
print(5 is not 5)

# Membership Operator
print("membership operators")
list = [3, 24, 2, 39, 378, 23, 83]
print(324 not in list)

# Bitwise Operators
